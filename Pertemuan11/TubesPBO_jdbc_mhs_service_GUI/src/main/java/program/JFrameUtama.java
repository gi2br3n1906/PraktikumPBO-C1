    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        String idText = idEdit.getText();
        String nama = namaEdit.getText();
        
        if (!idText.isEmpty() && !nama.isEmpty()) {
            try {
                // Extract numeric ID from "ID123" format
                int id = Integer.parseInt(idText.replace("ID", ""));
                
                // Create Mahasiswa object with updated data
                Mahasiswa mhs = new Mahasiswa(id, nama);
                
                // Update in database
                mahasiswaService.update(mhs);
                System.out.println("Updated in database: ID=" + id + ", Nama=" + nama);
                
                // Refresh list from database
                ambilSemuaDataButtonActionPerformed(evt);
                
                // Clear input fields
                idEdit.setText("");
                namaEdit.setText("");
            } catch (NumberFormatException e) {
                System.out.println("Invalid ID format");
            }
        } else {
            System.out.println("ID and Nama cannot be empty");
        }
    }//GEN-LAST:event_editButtonActionPerformed

