    private void AddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtonActionPerformed
        String nama = namaInput.getText();
        if (!nama.isEmpty()) {
            // Create new Mahasiswa object
            Mahasiswa mhs = new Mahasiswa();
            mhs.setNama(nama);
            
            // Add to database
            mahasiswaService.add(mhs);
            System.out.println("Added to database: " + nama);
            
            // Refresh list from database
            ambilSemuaDataButtonActionPerformed(evt);
            
            // Clear input field
            namaInput.setText("");
        } else {
            System.out.println("Nama cannot be empty");
        }
    }//GEN-LAST:event_AddButtonActionPerformed

