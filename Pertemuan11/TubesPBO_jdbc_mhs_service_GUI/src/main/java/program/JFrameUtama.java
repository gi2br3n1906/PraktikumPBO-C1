    private void ambilSemuaDataButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ambilSemuaDataButtonActionPerformed
        // Load all data from database
        List<Mahasiswa> mahasiswaList = mahasiswaService.getAll();
        
        // Clear current lists
        dlmID.clear();
        dlmNama.clear();
        
        // Populate lists with database data
        for (Mahasiswa mhs : mahasiswaList) {
            dlmID.addElement("ID" + mhs.getId());
            dlmNama.addElement(mhs.getNama());
        }
        
        System.out.println("Data loaded from database: " + mahasiswaList.size() + " records");
    }//GEN-LAST:event_ambilSemuaDataButtonActionPerformed

    private void namaInputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_namaInputActionPerformed
        String nama = namaInput.getText();
        if (!nama.isEmpty()) {
            dlmID.addElement("ID" + (dlmID.getSize() + 1)); // Auto-generate ID
            dlmNama.addElement(nama);
            System.out.println("Added Nama: " + nama);
            namaInput.setText(""); // Clear input field after action
        } else {
            System.out.println("Nama cannot be empty");
        }

    }//GEN-LAST:event_namaInputActionPerformed

